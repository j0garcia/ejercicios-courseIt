const y = 423423423423;

if (x == y) {
  console.log(`Los números ${x} y ${y} son iguales!`);
} else if (x > y) {
  console.log(`El número ${x} es mayor que el número ${y}`);
} else {
  console.log(`El número ${y} es mayor que el número ${x}`);
}